package eg.edu.alexu.csd.datastructure.mailServer;

public class Pair {
	private Object First;
	private Object Second;
	public Pair(Object first,Object second) {
		First=first;
		Second =second;
	}
	public Object getFirst() {
		return First;
	}
	public Object getSecond() {
		return Second;
	}
}
